public class DirectStore {
    public static void main(String[] args){
        /*
        OUTPUT: marks[0] 92
                marks[1] 85
                marks[2] 95
         */
        int marks[] = new int[3];
        marks[0] = 92;
        marks[1] = 85;
        marks[2] = 95;

        System.out.println("marks[0] "+ marks[0]);
        System.out.println("marks[1] "+ marks[1]);
        System.out.println("marks[2] "+ marks[2]);
    }
}
